#include<bits/stdc++.h>
using namespace std;
int a[100005];
long long b[100005];
int main(){
int n,k;
cin>>n>>k;
long long pos=0;
for(int i=0;i<n;i++)cin>>a[i];
for(int i=0;i<n;i++){
    int x;
    cin>>x;
    if(x==1){
            b[i+1]=b[i];
            pos+=a[i];
            a[i]=0;
    }
    else{
        b[i+1]=b[i]+a[i];
    }
}
long long rem=-1;
for(int i=0;i<=n-k;i++){

    rem=max(rem,b[i+k]-b[i]);
}
cout<<rem+pos<<endl;
return 0;
}























































































































































































































































































































































































